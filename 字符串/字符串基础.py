"""
字符串的存储结构主要分为顺序存储结构和链式存储结构两类

针对于单模式字符串匹配问题，常见算法：
朴素算法

  Brute Force 算法（暴力匹配算法）
    时间复杂度：O(n×m)
    空间复杂度：O(1)
    特点：简单直观，但效率较低
    适用场景：模式串较短或对性能要求不高的场景

基于前缀搜索的方法

  KMP 算法（Knuth-Morris-Pratt 算法）
    时间复杂度：O(n+m)
    空间复杂度：O(m)
    特点：利用失配信息避免重复比较，从前向后逐个读取文本字符
    适用场景：对性能要求较高的场景

基于后缀搜索的方法

  Boyer Moore 算法
    时间复杂度：平均 O(n/m)，最坏 O(n×m)
    空间复杂度：O(k)（k 为字符集大小）
    特点：从右到左比较，跳跃能力强
    适用场景：模式串较长，字符集较小的场景

  Horspool 算法
    时间复杂度：平均O(n)，最坏 O(n×m)
    空间复杂度：O(k)
    特点：BM算法的简化版本，实现简单
    适用场景：需要简单实现的场景

  Sunday 算法
    时间复杂度：平均O(n)，最坏 O(n×m)
    空间复杂度：O(k)
    特点：从左到右比较，跳跃能力强
    适用场景：需要从左到右匹配的场景

基于子串搜索的方法

  Rabin Karp 算法
    时间复杂度：平均O(n+m)，最坏O(n×m)
    空间复杂度：O(1)
    特点：使用滚动哈希，平均性能较好
    适用场景：需要处理多个模式串或对哈希冲突不敏感的场景

    
基于多模式字符串匹配问题，常见算法（重点掌握：字典树，AC自动机算法）：
前缀结构类方法

  字典树（Trie）
    时间复杂度：构建 O(k)，单次查找 O(m)
    空间复杂度：O(k)
    特点：支持高效的前缀匹配和批量字符串检索，结构直观，易于实现
    适用场景：前缀匹配、字符串集合检索、词频统计等

  AC 自动机
    时间复杂度：构建 O(k)，匹配 O(n+k)
    空间复杂度：O(k)
    特点：在字典树基础上引入失败指针，结合 KMP 失配思想，可一次遍历文本高效匹配所有模式串
    适用场景：多模式串精确匹配，如敏感词过滤、病毒特征检测等

后缀结构类方法
  后缀数组
    时间复杂度：构建O(nlogn)，查找O(m+logn)
    空间复杂度：O(n)
    特点：支持后缀的快速排序和二分查找，适合子串定位和重复子串分析，实现相对简单
    适用场景：子串查找、最长重复子串、最长公共子串等

  后缀树
    时间复杂度：构建 O(n)（理论），实际实现较复杂
    空间复杂度：O(n)，但常数较大
    特点：支持复杂的字符串分析，能高效解决多种字符串问题，但实现难度高、空间消耗大
    适用场景：复杂的字符串分析、需要多种子串关系查询的场景（实际多用后缀数组替代）

哈希与子串搜索类方法
  Rabin-Karp 算法
    时间复杂度：平均O(n+m)，最坏O(n×m)
    空间复杂度：O(1)
    特点：利用滚动哈希批量比对多个模式串，平均性能较好，但哈希冲突时退化
    适用场景：需要处理多个模式串、对哈希冲突不敏感的场景   
      
"""

def strcmp(str1, str2):
    """
    比较两个字符串的大小。
    返回值：
        -1：str1 < str2
         0：str1 == str2
         1：str1 > str2
    """
    # 逐字符比较
    i = 0
    while i < len(str1) and i < len(str2):
        c1 = ord(str1[i])
        c2 = ord(str2[i])
        if c1 < c2:
            return -1  # str1 当前字符小于 str2
        elif c1 > c2:
            return 1   # str1 当前字符大于 str2
        i += 1

    # 如果前面都相等，比较长度
    if len(str1) < len(str2):
        return -1  # str1较短
    elif len(str1) > len(str2):
        return 1   # str1较长
    else:
        return 0   # 完全相等