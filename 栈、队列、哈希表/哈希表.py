"""
哈希表总结:

本文系统梳理了哈希表的基本原理与核心技术要点，内容涵盖哈希表的定义、哈希函数的设计、哈希冲突的本质及主流冲突解决策略。

哈希表（Hash Table）：一种通过哈希函数将关键字key 映射到存储位置，实现高效查找、插入和删除的数据结构。

哈希函数（Hash Function）：用于将关键字转换为哈希表索引的函数，其优劣直接影响哈希表的性能和冲突概率。

哈希冲突（Hash Collision）：指不同关键字经过哈希函数后映射到同一地址，是哈希表设计中不可避免的问题。

哈希表的设计与实现主要关注两个核心问题：

哈希函数的构建：常见方法包括直接定址法、除留余数法、平方取中法、基数转换法、数字分析法、折叠法、随机数法、乘积法、点积法等。合理选择和设计哈希函数有助于均匀分布关键字，降低冲突概率。

哈希冲突的解决：
 主流方法有两类，开放地址法和链地址法。

   开放地址法：通过探查其他空槽位存放冲突元素，常见探查方式有线性探查、二次探查和伪随机探查等。

   链地址法：将哈希地址相同的元素以链表形式存储在同一槽位，结构灵活，实际应用更为广泛。
"""


#代码复现

#在Python中，哈希表是通过内置的`dict`类型来实现的。`dict`（字典）是一种键值对（key-value pair）的数据结构，其中键是唯一的，并且可以通过哈希函数将键映射到值的位置，从而实现快速查找、插入和删除操作。


#创建字典

my_dict = {}  # 创建一个空字典
my_dict = {'apple': 1, 'banana': 2}  # 创建一个包含键值对的字典


#添加或修改元素

my_dict['cherry'] = 3  # 添加一个新的键值对
my_dict['apple'] = 4  # 修改已存在的键的值


#访问元素

print(my_dict['apple'])  # 输出值4


#删除元素

del my_dict['banana']  # 删除键为'banana'的键值对
 

#检查键是否存在

if 'apple' in my_dict:
    print("Apple is in the dictionary.")
   

#遍历字典
   
for key, value in my_dict.items():
    print(f"{key}: {value}")
   

#获取字典的长度
print(len(my_dict))  # 输出字典中键值对的数量

#清空字典
my_dict.clear()  # 清空字典中的所有键值对
